package net.runelite.api;

import lombok.Getter;

/**
 * Enumeration of the varbits arrays used for the rune pouch.
 */
@Getter
public enum RunePouchVarbits
{
	AMOUNTS(Varbits.RUNE_POUCH_AMOUNT1, Varbits.RUNE_POUCH_AMOUNT2, Varbits.RUNE_POUCH_AMOUNT3),
	RUNES(Varbits.RUNE_POUCH_RUNE1, Varbits.RUNE_POUCH_RUNE2, Varbits.RUNE_POUCH_RUNE3);

	private final Varbits[] varbits;

	RunePouchVarbits(Varbits... varbits)
	{
		this.varbits = varbits;
	}
}
